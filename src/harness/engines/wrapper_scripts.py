POSIX_WRAPPER_TEMPLATE = """\
#!/bin/bash
# {wrapper_name} -- generated by 'harness engines install-wrappers'
# Description: {description}
# Endpoint: {endpoint}
# Default model: {default_model}
#
# This wrapper invokes the real ``claude`` binary with ANTHROPIC_BASE_URL
# redirected at the provider's Anthropic-compatible endpoint.  The wire-
# level HTTP call carries Claude Code's legitimate ``claude/<version>``
# User-Agent, satisfying the provider's allowlist.  TOS-compliant: no
# spoofing, the binary IS Claude Code.
#
# Set ${key_env} in your shell before invoking.  The wrapper does NOT
# embed any secret.

if [ -z "${{{key_env}}}" ]; then
  echo "Error: {key_env} is not set." >&2
  echo "Set it in your shell environment first:" >&2
  echo "  export {key_env}=your-api-key" >&2
  exit 64
fi

exec env \\
  ANTHROPIC_BASE_URL="{endpoint}" \\
  ANTHROPIC_AUTH_TOKEN="${{{key_env}}}" \\
  ANTHROPIC_API_KEY="${{{key_env}}}" \\
  ANTHROPIC_MODEL="{default_model}" \\
  ANTHROPIC_DEFAULT_SONNET_MODEL="{default_model}" \\
  ANTHROPIC_DEFAULT_OPUS_MODEL="{default_model}" \\
  ANTHROPIC_DEFAULT_HAIKU_MODEL="{default_model}" \\
  CLAUDE_CODE_SUBAGENT_MODEL="{default_model}" \\
  ENABLE_TOOL_SEARCH="false" \\
  claude --bare "$@"
"""


