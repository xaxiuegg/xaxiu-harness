"""W14-CLAUDE-CODE-WRAPPER-SCRIPTS 2026-05-26: generate per-provider
wrapper scripts that pre-configure ``claude`` for a specific provider.

Each wrapper is a thin shell/cmd script that:
  - Sets ``ANTHROPIC_BASE_URL``, ``ANTHROPIC_AUTH_TOKEN``, model aliases
  - exec's the real ``claude`` binary with ``--bare``

Wrappers contain ONLY URLs (public knowledge) and references to env-var
names (operator's actual keys stay in their shell env).  No secrets in
wrapper files.

Result: operator types ``claude-mimo "prompt"`` in any terminal and
gets MiMo via Claude Code with the legitimate ``claude/<version>`` UA
— without ever needing to set ANTHROPIC_BASE_URL manually.

Usage:
  ``harness engines install-wrappers``    install for available providers
  ``harness engines list-wrappers``       show what's installed

The harness CLI commands are wired in ``cli.py``; this module exposes
the data layer (templates, install logic, listing).
"""
