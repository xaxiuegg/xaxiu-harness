def _is_windows() -> bool:
    """W14-CLAUDE-CODE-WRAPPER-SCRIPTS: detect if we're on Windows.

    POSIX path is used on Linux/Mac/Git-Bash.  Windows .cmd is generated
    only when ``platform.system() == "Windows"`` AND the operator hasn't
    forced POSIX via ``HARNESS_WRAPPER_FORMAT=posix``.
    """
    forced = os.environ.get("HARNESS_WRAPPER_FORMAT", "").lower()
    if forced == "windows":
        return True
    if forced == "posix":
        return False
    return platform.system() == "Windows"


def _render_wrapper(
    wrapper_name: str,
    spec: dict[str, str],
    *,
    use_windows: Optional[bool] = None,
) -> tuple[str, str]:
    """Render a wrapper script.  Returns (filename, contents).

    ``use_windows=None`` auto-detects; explicit True/False is for tests.
    """
    win = use_windows if use_windows is not None else _is_windows()
    template = WINDOWS_WRAPPER_TEMPLATE if win else POSIX_WRAPPER_TEMPLATE
    suffix = ".cmd" if win else ""
    filename = f"{wrapper_name}{suffix}"

    engine_key = spec["engine_key"]
    endpoint = WRAPPER_PROVIDER_ENDPOINTS.get(engine_key, "")
    default_model = WRAPPER_DEFAULT_MODELS.get(engine_key, "")

    contents = template.format(
        wrapper_name=wrapper_name,
        description=spec["description"],
        endpoint=endpoint,
        default_model=default_model,
        key_env=spec["key_env"],
    )
    return filename, contents


def install_wrappers(
    *,
    target_dir: Optional[Path] = None,
    only: Optional[list[str]] = None,
    use_windows: Optional[bool] = None,
    overwrite: bool = True,
) -> dict[str, dict]:
    """Generate + install all (or selected) wrapper scripts to disk.

    Args:
      target_dir: directory to install into.  Defaults to ~/.harness/bin.
      only: list of wrapper names (e.g. ["claude-mimo", "claude-qwen"])
        to install; ``None`` installs all defined wrappers.
      use_windows: force Windows / POSIX wrapper format.  ``None``
        auto-detects.
      overwrite: if False, skip wrappers that already exist.

    Returns a dict mapping each wrapper name to ``{"path": str,
    "status": "installed" | "skipped" | "skipped-exists", "endpoint":
    str, "default_model": str}``.
    """
    dest = target_dir or DEFAULT_WRAPPER_DIR
    dest.mkdir(parents=True, exist_ok=True)

    selected = only if only is not None else list(WRAPPER_DEFINITIONS.keys())
    result: dict[str, dict] = {}
    for wrapper_name in selected:
        spec = WRAPPER_DEFINITIONS.get(wrapper_name)
        if spec is None:
            result[wrapper_name] = {
                "status": "skipped",
                "reason": f"unknown wrapper: {wrapper_name}",
                "path": None,
            }
            continue
        filename, contents = _render_wrapper(
            wrapper_name, spec, use_windows=use_windows,
        )
        path = dest / filename
        if path.exists() and not overwrite:
            result[wrapper_name] = {
                "status": "skipped-exists",
                "path": str(path),
                "endpoint": WRAPPER_PROVIDER_ENDPOINTS.get(
                    spec["engine_key"], "",
                ),
                "default_model": WRAPPER_DEFAULT_MODELS.get(
                    spec["engine_key"], "",
                ),
            }
            continue
        path.write_text(contents, encoding="utf-8")
        # On POSIX, mark executable
        if not (use_windows if use_windows is not None else _is_windows()):
            try:
                path.chmod(path.stat().st_mode | stat.S_IXUSR
                           | stat.S_IXGRP | stat.S_IXOTH)
            except (OSError, NotImplementedError):
                # Best effort; chmod can fail on some Windows-emulation
                # filesystems even when we're in posix mode for tests.
                pass
        result[wrapper_name] = {
            "status": "installed",
            "path": str(path),
            "endpoint": WRAPPER_PROVIDER_ENDPOINTS.get(
                spec["engine_key"], "",
            ),
            "default_model": WRAPPER_DEFAULT_MODELS.get(
                spec["engine_key"], "",
            ),
        }
    return result


def list_wrappers(
    target_dir: Optional[Path] = None,
) -> list[dict]:
    """Return per-wrapper installation status.

    Each entry has: name, expected_path, installed (bool),
    description, endpoint, default_model, key_env.
    """
    dest = target_dir or DEFAULT_WRAPPER_DIR
    suffix = ".cmd" if _is_windows() else ""
    out = []
    for wrapper_name, spec in WRAPPER_DEFINITIONS.items():
        engine_key = spec["engine_key"]
        path = dest / f"{wrapper_name}{suffix}"
        out.append({
            "name": wrapper_name,
            "expected_path": str(path),
            "installed": path.exists(),
            "description": spec["description"],
            "endpoint": WRAPPER_PROVIDER_ENDPOINTS.get(engine_key, ""),
            "default_model": WRAPPER_DEFAULT_MODELS.get(engine_key, ""),
            "key_env": spec["key_env"],
            "key_present": bool(os.environ.get(spec["key_env"])),
        })
    return out


def get_path_hint() -> Optional[str]:
    """Return a hint about adding the wrapper dir to PATH if missing.
    ``None`` when the dir is already on PATH.  Used by CLI to guide
    the operator after install.
    """
    dest = str(DEFAULT_WRAPPER_DIR.resolve())
    current_path = os.environ.get("PATH", "")
    path_components = current_path.split(os.pathsep)
    if dest in path_components:
        return None
    if _is_windows():
        return (
            f"Add the wrapper dir to PATH:\n"
            f"  PowerShell:  [Environment]::SetEnvironmentVariable("
            f"\"PATH\", \"$env:PATH;{dest}\", [EnvironmentVariableTarget]::User)\n"
            f"  cmd:         setx PATH \"%PATH%;{dest}\""
        )
    return (
        f"Add the wrapper dir to PATH (add to ~/.bashrc or ~/.zshrc):\n"
        f"  export PATH=\"$PATH:{dest}\""
    )
